/**
 * Privacy Service - Manages user anonymity and data controls
 * Implements Rule 1: Privacy First — Never compromise anonymity
 * Implements Rule 6: POPIA compliant
 */

import * as SecureStore from 'expo-secure-store';
import * as Crypto from 'expo-crypto';

// Storage keys
const ANONYMOUS_ID_KEY = 'pshad_anonymous_id';
const ANONYMOUS_ID_CREATED_KEY = 'pshad_anonymous_id_created';

// ---- Anonymous ID Management ----

/**
 * Get or create anonymous device ID
 * This ID is:
 * - Persistent across app restarts
 * - Not linked to any personal information
 * - Stored securely on device only
 * - Can be regenerated by user
 */
export async function getAnonymousId(): Promise<string> {
  try {
    // Try to get existing ID
    const existingId = await SecureStore.getItemAsync(ANONYMOUS_ID_KEY);
    
    if (existingId) {
      return existingId;
    }
    
    // Generate new ID if none exists
    return await generateNewAnonymousId();
  } catch (error) {
    console.error('[PrivacyService] Error getting anonymous ID:', error);
    // Return a temporary session ID if secure store fails
    return generateTemporaryId();
  }
}

/**
 * Generate and store a new anonymous ID
 * Call this when user wants to "reset" their anonymous identity
 */
export async function generateNewAnonymousId(): Promise<string> {
  try {
    // Generate cryptographically secure random ID
    const randomBytes = await Crypto.getRandomBytesAsync(16);
    const hexString = Array.from(randomBytes)
      .map(b => b.toString(16).padStart(2, '0'))
      .join('');
    
    // Format: anon_XXXX...XXXX (32 hex chars)
    const newId = `anon_${hexString}`;
    
    // Store securely
    await SecureStore.setItemAsync(ANONYMOUS_ID_KEY, newId);
    await SecureStore.setItemAsync(ANONYMOUS_ID_CREATED_KEY, new Date().toISOString());
    
    console.log('[PrivacyService] New anonymous ID generated');
    return newId;
  } catch (error) {
    console.error('[PrivacyService] Error generating anonymous ID:', error);
    return generateTemporaryId();
  }
}

/**
 * Get creation date of anonymous ID
 */
export async function getAnonymousIdCreatedDate(): Promise<string | null> {
  try {
    return await SecureStore.getItemAsync(ANONYMOUS_ID_CREATED_KEY);
  } catch {
    return null;
  }
}

/**
 * Generate temporary ID (fallback when secure store unavailable)
 */
function generateTemporaryId(): string {
  const timestamp = Date.now().toString(36);
  const random = Math.random().toString(36).substring(2, 10);
  return `temp_${timestamp}_${random}`;
}

/**
 * Format anonymous ID for display (truncated)
 * Shows first 8 and last 4 characters
 */
export function formatAnonymousId(id: string): string {
  if (id.length <= 16) return id;
  
  const prefix = id.substring(0, 12);
  const suffix = id.substring(id.length - 4);
  return `${prefix}...${suffix}`;
}

