// ---- Data Controls ----

/**
 * Clear all local app data
 * This removes:
 * - Anonymous ID (will regenerate)
 * - Push notification token
 * - Any cached data
 */
export async function clearAllLocalData(): Promise<boolean> {
  try {
    // Clear anonymous ID
    await SecureStore.deleteItemAsync(ANONYMOUS_ID_KEY);
    await SecureStore.deleteItemAsync(ANONYMOUS_ID_CREATED_KEY);
    
    // Clear push token
    await SecureStore.deleteItemAsync('pshad_push_token');
    
    console.log('[PrivacyService] All local data cleared');
    return true;
  } catch (error) {
    console.error('[PrivacyService] Error clearing data:', error);
    return false;
  }
}

